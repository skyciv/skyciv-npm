/**
 *
 * @param {Object} from
 * @param {Object} to
 */
function setAttribs(from, to) {
	Object.entries(from).forEach(([collection, collectionValues]) => {
		if (typeof collectionValues === 'function' || typeof collectionValues === 'object') {
			// Check if a single item should be created
			if (Object.keys(keyClassMap).includes(collection)) {
				Object.keys(collectionValues).forEach((id) => {
					to[collection][id] = new keyClassMap[collection]();
				});
			}

			// Dig deeper
			if (collectionValues !== null) {
				if (collection === 'sections') {
					// Special treatment for sections
					Object.entries(collectionValues).forEach(([id, secData]) => {
						if (Array.isArray(secData)) {
							to[collection][id].load_section = secData;
						} else if (secData === null) {
							to[collection][id] = null;
						} else {
							to[collection][id].load_section =
								secData.aux.polygons[0].library_selections;
						}
						to[collection][id].material_id = secData.material_id;
					});
				} else {
					if (!to.hasOwnProperty(collection)) {
						if (Array.isArray(from[collection])) { 
							to[collection] = [];
						} else {
							to[collection] = {};
						}
					}
					setAttribs(from[collection], to[collection]);
				}
			}
		} else {
			// Set the value
			to[collection] = collectionValues;
		}
	});
}

function modelAdapter(jsonModel, model) {
	setAttribs(jsonModel, model);
}

